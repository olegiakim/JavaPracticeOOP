package task1.Robot;

// class
public class Robot {

    String name;
    String model;
    int height;
    String pantColor;
    String tShirt;
    boolean isCap;

    // constructor
    public Robot(String name, String model, int height, String pantColor, String tShirt, boolean isCap) {
        this.name = name;
        this.model = model;
        this.height = height;
        this. pantColor = pantColor;
        this.tShirt = tShirt;
        this.isCap = isCap;
    }



}
